package bitwise;

public class Right_Shift {

	public static void main(String[] args) {
		int a=10; //1010
		int b=15; //1111
		int c=20; //10100
		
		System.out.println(a>>2); //0010 =2
		System.out.println(a>>3); //0001 =1
		System.out.println(b>>2); //0011 =3
		System.out.println(c>>5); //00000=0

	}

}
