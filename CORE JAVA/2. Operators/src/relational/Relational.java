package relational;

public class Relational {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		int c=20;
		
		System.out.println(a>b);  // True
		System.out.println(a<b);  // False
		System.out.println(a==b); // False
		System.out.println(a!=b); // True
		System.out.println(c>=b); // True
		System.out.println(c<=b); // False

	}

}
